#include <unistd.h>

void	ft_swap(int *a, int *b)
{
	int a;
	int b;

	*a = '1';
	*b = '2';
	*a = *b;
	*b = *a;
}

int main(void)
{
	write(1, *a, 1);
	write(1, *b, 1);
}
